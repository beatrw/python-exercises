#Estudando tuplas
#Imutáveis - não adiciona item, não remove, não atribui

tupla = ('aa', 33, 'bb')
print(tupla)
print(tupla[0])
print(len(tupla))
print(tupla.index(33))

#Slicing 
print(tupla[1:])

#Deletar a tupla
del tupla
#print(tupla)

#Conversão de tupla em lista
tupla1 = (1, 2, 3)
lista = list(tupla1)
lista.append(4)
print(lista)

#Conversão de lista para tupla
tupla2 = tuple(lista) 
print(tupla2)