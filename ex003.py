#Operações aritméticas
print(5 + 2) #adição
print(5 - 2) #subtração
print(5 * 2) #multipllicação
print(5 / 2) #divisão
print(5 ** 2) #potenciação
print(5 // 2) #divisão inteira
print(5 % 2) #mod/resto da divisão

#Ordem de precedência de operações matemáticas:
# 1: ()
# 2: **
# 3: * / // %
# 4: + -

